// ALCUNI METODI CON IL MATH

public class java08math {
    public static void main(String[] args) {

        System.out.println(Math.abs(-55)); //absolut sempre positivo
        System.out.println(Math.min(55,22)); //minimo il numero + basso
        System.out.println(Math.max(55,22));
        System.out.println(Math.pow(5,2));  // potenza di un numero
        System.out.println(Math.sqrt(25));  // radice quadrata "square root"
        System.out.println(Math.PI);  // PI GRECO

    }
}
