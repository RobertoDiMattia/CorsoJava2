/* operatori di comparazione
 < MINORE    <=  MINORE UGUALE
 > MAGGIORE  >= MAGGIORE UGUALE
 == UGUALE   != DIVERSO
 */

public class java09opcompara {
    public static void main(String[] args) {

        boolean risultato = 11 > 10;
        boolean risultato1 = 11 >= 10;
        boolean risultato2 = 11 < 11;
        boolean risultato3 = 11 <= 11;
        boolean risultato4 = 11 == 10;
        boolean risultato5 = 11 != 10;

        System.out.println(risultato);
        System.out.println(risultato1);
        System.out.println(risultato2);
        System.out.println(risultato3);
        System.out.println(risultato4);
        System.out.println(risultato5);
    }
}
